/// 将 shenfen_id 编码为固定 48 字节机构标识（右侧补零）。
pub fn shenfen_id_to_fixed48(shenfen_id: &str) -> Option<[u8; 48]> {
    let raw = shenfen_id.as_bytes();
    if raw.is_empty() || raw.len() > 48 {
        return None;
    }
    let mut out = [0u8; 48];
    out[..raw.len()].copy_from_slice(raw);
    Some(out)
}

